void	ft_swap(int *a, int *b)
{
	int swap;

	swap = *a;
	*a = *b;
	*b = swap;
}

void	ft_rev_int_tab(int *tab, int size)
{
	int i;

	i = -1;
	while (++i < --size)
	{
		ft_swap(&tab[i], &tab[size]);
	}
}
